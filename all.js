document.addEventListener('DOMContentLoaded', function() {
    const expandingPara = document.querySelector('.expandingPara');
    
    // Wait for a short moment (e.g., 100ms) to start the expansion.
    // This is to ensure that CSS has been fully applied.
    setTimeout(() => {
        expandingPara.style.maxWidth = "100%";
        expandingPara.style.maxHeight = "100%";
        expandingPara.style.opacity = "1";
    }, 100);
});

